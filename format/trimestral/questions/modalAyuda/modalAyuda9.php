					</div>
					<div class="textoAyuda" >
					9.1. Número de imputados a los que se les impuso prisión preventiva oficiosa.- al número de imputados a los que se les dictó el auto de vinculación a proceso o que se encontraban en espera de la audiencia de vinculación y que se les impuso la prisión preventiva por encontrarse en los casos de delincuencia organizada, homicidio doloso, violación, secuestro, trata de personas, delitos cometidos con medios violentos como armas y explosivos, así como delitos graves que determine la ley en contra de la seguridad de la nación, el libre desarrollo de la personalidad y de la salud, de conformidad con lo dispuesto en el artículo 19 de la Constitución Política de los Estados Unidos Mexicános y 167, sexto párrafo del CNPP;
					</div>
					<div class="textoAyuda" >
					9.2. Número de imputados a los que se les impuso prisión preventiva no oficiosa.- al número de imputados a los que se les dictó el auto de vinculación a proceso o que se encontraban en espera de la audiencia de vinculación y que se les impuso la prisión preventiva por considerarse que otras medidas cautelares no son suficientes para garantizar la comparecencia del imputado en el juicio, el desarrollo de la investigación, la protección de la víctima, de los testigos o de la comunidad, así como cuando el imputado esté siendo procesado o haya sido sentenciado previamente por la comisión de un delito doloso, de conformidad con lo dispuesto en el artículo 19 de la Constitución Política de los Estados Unidos Mexicanos y 167, primer párrafo del CNPP;
					</div>
					<div class="textoAyuda" >
					9.3. Número de imputados a los que se les impuso otra medida cautelar distinta.- al número de imputados a los que se les dictó el auto de vinculación a proceso o que se encontraban en espera de la audiencia de vinculación y que se les impuso alguna de los tipos de medidas cautelares dispuestos en el artículo 155, fracciones I a la XIII del CNPP, y
					</div>
